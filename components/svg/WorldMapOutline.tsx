export default function WorldMapOutline({ issLat, issLon, animate }: WorldMapOutlineProps) {
  const w = 800;
  const h = 400;

  // ISS orbital path (sinusoidal, 51.6 degree inclination)
  const orbitAmplitude = (51.6 / 90) * (h / 2);
  const orbitPoints: string[] = [];
  for (let i = 0; i <= 200; i++) {
    const t = i / 200;
    const ox = t * w;
    const oy = h / 2 - Math.sin(t * 2 * Math.PI) * orbitAmplitude;
    orbitPoints.push(`${ox.toFixed(1)},${oy.toFixed(1)}`);
  }
  const orbitPath = `M ${orbitPoints.join(" L ")}`;

  // Convert lat/lon to x,y
  const issX = ((issLon + 180) / 360) * w;
  const issY = ((90 - issLat) / 180) * h;

  return (
    <svg
      viewBox={`0 0 ${w} ${h}`}
      width="100%"
      fill="none"
      aria-hidden="true"
      className="rounded-lg overflow-hidden"
      style={{ backgroundColor: "rgba(15, 23, 42, 0.02)" }}
    >
      {/* Ocean texture - subtle latitude lines */}
      {[60, 120, 200, 280, 340].map((y) => (
        <line
          key={`lat-${y}`}
          x1="0"
          y1={y}
          x2={w}
          y2={y}
          stroke="var(--fg-muted)"
          strokeWidth="0.3"
          strokeDasharray="2 6"
          opacity={0.08}
        />
      ))}
      {/* Longitude lines */}
      {[160, 320, 480, 640].map((x) => (
        <line
          key={`lon-${x}`}
          x1={x}
          y1="0"
          x2={x}
          y2={h}
          stroke="var(--fg-muted)"
          strokeWidth="0.3"
          strokeDasharray="2 6"
          opacity={0.08}
        />
      ))}

      {/* Equator */}
      <line
        x1="0"
        y1={h / 2}
        x2={w}
        y2={h / 2}
        stroke="var(--fg-muted)"
        strokeWidth="0.4"
        strokeDasharray="4 4"
        opacity={0.15}
      />
      {/* Prime meridian */}
      <line
        x1={w / 2}
        y1="0"
        x2={w / 2}
        y2={h}
        stroke="var(--fg-muted)"
        strokeWidth="0.4"
        strokeDasharray="4 4"
        opacity={0.15}
      />

      {/* Continent fills */}
      {CONTINENTS.map((c) => (
        <path
          key={`fill-${c.id}`}
          d={c.d}
          fill="var(--fg-muted)"
          opacity={0.06}
          strokeLinejoin="round"
        />
      ))}

      {/* Continent outlines */}
      {CONTINENTS.map((c) => (
        <path
          key={`stroke-${c.id}`}
          d={c.d}
          stroke="var(--fg-muted)"
          strokeWidth="1"
          fill="none"
          opacity={0.35}
          strokeLinejoin="round"
          strokeLinecap="round"
        />
      ))}

      {/* ISS Orbital path */}
      <path
        d={orbitPath}
        stroke="var(--orbital)"
        strokeWidth="1.2"
        strokeDasharray="6 4"
        fill="none"
        opacity={animate ? 0.35 : 0}
        style={{ transition: "opacity 0.8s ease-out 0.3s" }}
      />

      {/* ISS animated dot */}
      {animate && (
        <>
          <circle r="10" fill="none" stroke="var(--orbital)" strokeWidth="1" opacity={0.2}>
            <animateMotion dur="8s" repeatCount="indefinite" path={orbitPath} />
            <animate attributeName="r" values="8;14;8" dur="2s" repeatCount="indefinite" />
          </circle>
          <circle r="4" fill="var(--orbital)" style={{ filter: "drop-shadow(0 0 6px rgba(37, 99, 235, 0.5))" }}>
            <animateMotion dur="8s" repeatCount="indefinite" path={orbitPath} />
            <animate attributeName="r" values="3;5;3" dur="2s" repeatCount="indefinite" />
          </circle>
        </>
      )}

      {/* Static ISS marker */}
      {!animate && (
        <circle cx={issX} cy={issY} r="4" fill="var(--orbital)" opacity={0.4} />
      )}

      {/* ISS label */}
      {animate && (
        <text
          x="12"
          y={h - 12}
          fill="var(--orbital)"
          fontSize="11"
          fontFamily="monospace"
          opacity={0.5}
        >
          ISS ORBITAL TRACK
        </text>
      )}
    </svg>
  );
}
